Ik ben Yasmine, oprichter van deze website.

Welkom op de site Worldwide!

Dit is een website waar mensen bij elkaar komen die van reizen houden en benieuwd zijn naar wat de ervaringen van andere mensen zijn op bepaalde plekken! 
Ook kunnen mensen op deze site een kijkje nemen als zij inspiratie op willen doen voor hun eigen vakantie aan de hand van recensies aan anderen.

Je kan op deze site een kijkje nemen zonder in te loggen, maar om een reactie te plaatsen en te kunnen reageren op elkaar moet u een account aanmaken.

Ik hoop dat mensen inspiratie op kunnen doen op deze website en dat mensen andere mensen kunnen helpen door middel van hun recensies.


